Map::Map() :
	m_CollLeft(false),
	m_CollRight(false),
	m_handle(-1),
	m_graphWidth(0),
	m_graphHeight(0),
	m_LeftNum(0),
	m_RightNum(0),
	m_LeftPos(0, 0),
	m_RightPos(0, 0),
	m_ScrollSpeed(0, 0),
	m_Vec(0,0),
	m_player(nullptr)
{
	for (int i = 0; i < kBgNumY; i++)
	{
		for (int j = 0; j < kBgNumX; j++)
		{
			m_MapData[i][j] = kMapData[i][j];
		}
	}

	
}

Map::~Map()
{
}

void Map::load()
{
	m_handle = LoadGraph(kFileName);
	GetGraphSize(m_handle, &m_graphWidth, &m_graphHeight);
}

void Map::unload()
{
	DeleteGraph(m_handle);
}

void Map::update()
{
	m_Vec.x = 0;
	m_Vec.y = 0;
	m_ScrollSpeed.x = 0;
	m_ScrollSpeed.y = 0;

	if (m_player->GetExist())
	{
		if (Pad::isPress(PAD_INPUT_RIGHT))
		{
			if (!m_CollRight)
			{
				m_Vec.x -= 5;
				if (Pad::isPress(PAD_INPUT_3))
				{
					m_Vec.x -= 5;
				}
			}
		}

		if (Pad::isPress(PAD_INPUT_LEFT))
		{
			if (!m_CollLeft)
			{
				m_Vec.x += 5;
				if (Pad::isPress(PAD_INPUT_3))
				{
					m_Vec.x += 5;
				}
			}
		}

		/*if (m_player->GetPos().x < Player::kFristPlayerPosX - 30)
		{
			m_Vec.x += m_player->GetMove();
		}*/

	
	//	m_Vec.x += m_player->GetMove();

		PlayerMove(m_player->GetKnockBack(), m_player->GetMove());

		m_ScrollSpeed = m_Vec;

		m_LeftPos += m_Vec;
	}

	m_CollLeft = false;
	m_CollRight = false;
}

void Map::draw()
{
	drawMap();
}

void Map::PlayerMove(float knockback, float Move)
{
	m_Vec.x -= knockback;
	m_Vec.x += Move;
}

int Map::chipNumX()
{
	return (m_graphWidth / kChipSize);
}

int Map::chipNumY()
{
	return (m_graphHeight / kChipSize);
}

int Map::chipNum()
{
	return(chipNumX() * chipNumY());
}

void Map::drawMap()
{
	for (int x = 0; x < kBgNumX; x++)
	{
		for (int y = 0; y < kBgNumY; y++)
		{
			int ChipNumY = kMapData[y][x] / 25;
			int ChipNumX = kMapData[y][x] % 25;
			/*DrawRectGraph(x * kChipSize, y * kChipSize,
				kChipSize * ChipNumX, kChipSize * ChipNumY,
				kChipSize, kChipSize, m_handle, true);*/

			DrawRectGraph(m_LeftPos.x + kChipSize * x, m_LeftPos.y + kChipSize * y,
				kChipSize * ChipNumX, kChipSize * ChipNumY,
				kChipSize, kChipSize, m_handle, true);
		}
	}
}
